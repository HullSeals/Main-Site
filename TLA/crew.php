            <p>“Brainiac”, “Nerd”, “A Walking Computer”... all of these and more are adjectives that have been applied to Horton. A constant overachiever, second best is no good for her, to the point of fault. She is always in a rush to be the first to do something or to improve even trivial aspects of the ship’s efficiency. The Limpet’s Call is the flagship, and Horton will make sure her crew keeps it that way. Quick to raise her voice, but always logical, she is one of the most notable personalities on the ship. Being chief engineer of a mega-ship was always a goal of hers - and so she jumped at the chance to join the crew of a carrier heading out to deep space. She is unhappy the ship is heading to a permanent mooring out in the black, however, regardless of her assignment, she’ll ensure her crew performs above and beyond expectations.</p>
