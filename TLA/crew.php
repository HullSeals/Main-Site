            <p>The incident on board the Vanguard has taken its toll on her, with both physical scarring a reminder but also the mental traumas of the attack. After resigning from the Imperial navy, she joined the crew of the Limpet's Call, dedicating her life to the Search and Rescue missions of the Seals, and to ensure the safe operation of ships within the sphere of influence of the Limpet’s Call.</p>
