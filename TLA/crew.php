            <p>Loud, confident, and capable of an incredible level of self-justification, he is often considered washed up by the rest of the crew. A functional drunk, he refuses to admit he has a problem to the point of isolating himself away when he is not needed. He took the offer to join the Limpet’s Call as a way to get out and explore again, feeling more at home in the void than anywhere else. However, when push comes to shove, he can shove, and will make the hard decision when needed.</p>
