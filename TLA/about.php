    <h1>About</h1>
    <br>
    <p><i>The Long Arf</i> is an expedition by the Hull Seals, centered around the deployment of the first of our three Fleet Carriers to the Ryker's Hope region of the galaxy, at Distant Worlds 2 Waypoint 9, an homage to the trip that gave rise to our organization. This carrier, HSRC Limpet's Call, the flagship of the Seal fleet, will be our organization's galactic headquarters and our home. In support of Fleetcomm's Deep Space Support Array, the carrier will take up station on the highway between Beagle Point and Sagittarius A*.</p>

    <p>The expedition will launch from the furthest carrier dock in the black, at Colonia, and head galactic north toward deployment, via Odin's Hold in a challenging voyage of logistics, exploration, scientific research, and camaraderie. The journey will be marked with a narrative journey, including the introduction of the whole crew of the HSRC Limpet's Call, and the adventures as she journies through the stars to her home, and exploring the broad field of activities available within Elite: Dangerous. These activities include fighter jousting (held outside travel PGs), SRV exploration, canyon racing, High-Gravity Landing competitions, mass-jumps, and more!</p>
